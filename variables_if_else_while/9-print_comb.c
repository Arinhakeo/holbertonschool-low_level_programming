#include <stdio.h>
/**
* main - Entry point
*
* Description: ll
*
*
* Return: Always 0 (Success)
*/
int main(void)
{
	int num;
	int vir;
	int spas;

	for (num = '0'; num <= '9'; num++)
	{
	putchar(num);
	}
	for (vir = ','; vir <= ','; vir++)
	{
	putchar(vir);
	}
	for (spas = ' '; spas <= ' '; spas++)
	{
	putchar(spas);
	}
	putchar('\n');
	return (0);
}
